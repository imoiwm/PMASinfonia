                History of Phi Mu Alpha Sinfonia
            </h2>    
            <p class="para">
                Phi Mu Alpha Sinfonia, Fraternity for Musical Men was
                founded October 6th, 1898, at the New England Conservatory
                in Boston, Massachusetts. Under the direction of founder
                Ossian Everett Mills, &Phi;MA Sinfonia has worked for over a
                century to advance music in America. The opportunity of becoming
                a Sinfonian is offered to as many as possible who, through a love
                for music, can assist in the fulfillment of the Fraternity's Object and ideals.</br>
            </p>
            <blockquote>
                <p style="text-align: center;">
                <span style="font-style: italic; color: #747474;">The Object of this Fraternity shall be for the development of the best and truest fraternal spirit; the mutual welfare and brotherhood of musical students; the advancement of music in America and a loyalty to the Alma Mater.</span></p>
            </blockquote>    
            <p class="para">
                In the beginning of the 20th century, American Music was considered
                inferior to the music being produced by European composers at the time.
                Sinfonia's goal was to further music in America to a point where American music
                was held with the same value and respect as its European
                counterparts. Working together, musical students strove to meet this goal, while working to better themselves
                at the same time. Ossian Everett Mills was devoted to
                those who joined Sinfonia to obtain those ideals. Through the 1960's, &Phi;MA Sinfonia 
                acted mainly on a professional fraternity, focusing on the advancement of musical students
                careers and service to American music. In the 1970's, the fraternity switched to become a social fraternity,
                while still maintaining it's focus on fulfilling the Object. 
            </p>    
